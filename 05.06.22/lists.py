#списки - это объекты, которые хранят в себе набор других объектов, например числа, строки и тд. 
#элементы списка нумеруются с 0 и эти номера называются индексами.
lst = [1,2,-4,5,-10] # давайте создадим список и положим его в переменную lst
plus = lst[0] + lst[1] # а затем возьмем из этого списка элементы с индексами 0 и 1 и сложим их, записав результат в переменную plus
print(plus)# теперь в plus лежит сумма нулевого и первого элементов списка, то есть 1+2 = 3
